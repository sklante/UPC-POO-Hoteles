    private boolean validacionCampos() {

        if (!(ControladorHotel.esVacio(txtNombre.getText())
                && ControladorHotel.esVacio(txtIdentificacion.getText())
                && ControladorHotel.esVacio(txtTelefono.getText())
                && ControladorHotel.esVacio(txtCorreoElectronico.getText())
                && ControladorHotel.esVacio(txtMedioDePago.getText()))
                && cbxTipoDocumento.getSelectedIndex() != -1) {
            return true;
        } else {
            JOptionPane.showMessageDialog(null, "Debe llenar todos los campos", "Error ingreso de Datos", 2);
            return false;
        }
    }

    private TipoDocumento[] tipos(){
       TipoDocumento[] tipoDocumentos = new TipoDocumento[4];
        tipoDocumentos[0] =(new TipoDocumento("DNI"));
        tipoDocumentos[1] =(new TipoDocumento("PASAPORTE"));
        tipoDocumentos[2] =(new TipoDocumento("RUC"));
        tipoDocumentos[3] =(new TipoDocumento("RUC-PERSONA"));
       return tipoDocumentos;
    }
    
    
