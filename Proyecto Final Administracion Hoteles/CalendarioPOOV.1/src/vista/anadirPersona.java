    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed

        if (validacionCampos()) {
            Documento documento = new Documento(txtIdentificacion.getText(), (TipoDocumento) cbxTipoDocumento.getSelectedItem());
            persona = new Persona(txtNombre.getText(),
                    documento, txtTelefono.getText(), txtCorreoElectronico.getText());
            persona.setDireccion(txtDireccion.getText());
            persona.setCiudad(txtCiudad.getText());
            persona.setPais(txtPais.getText());
            persona.setEstado(txtEstado.getText());
            if (empresa != null) {
                Persona empleado = (Persona)persona;
                empleado.setEntidadActual("Empresa: "+empresa.getNombre());
                empleado.setEntidadPagador(true);
                empresa.getContacto().add(empleado);
                this.dispose();
            } else if (agencia != null) {
                Persona pasajero = (Persona)persona;
                pasajero.setEntidadActual("Agencia: "+agencia.getNombre());
                pasajero.setEntidadPagador(true);
                agencia.getContacto().add(pasajero);
                this.dispose();
            } else if(!esEmpleado){
                Persona pasajero = (Persona)persona;
                pasajero.setEsPagadorPersona(true);
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Debe crear empresa", "Error ingreso de Datos", 2);
            }
        }
       ControladorHotel.personas.add(persona);
    }//GEN-LAST:event_btnAceptarActionPerformed

