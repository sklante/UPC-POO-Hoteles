    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed

        if (validacionCampos()) {
            if(agencia==null){
            Documento documento = new Documento(txtIdentificacion.getText(), (TipoDocumento) cbxTipoDocumento.getSelectedItem());
            agencia = new Agencia(txtNombreEmpresa.getText(),
                    documento, txtTelefono.getText(), txtCorreoElectronico.getText(),
                    new Medios(txtMedioDePago.getText()));
            agencia.setDireccion(txtDireccion.getText());
            agencia.setCiudad(txtCiudad.getText());
            agencia.setPais(txtPais.getText());
            agencia.setEstado(txtEstado.getText());
            agencia.setConvenio(txtConvenio.getText());
            }
            if (!agencia.getContacto().isEmpty()) {
                ControladorHotel.agencias.add(agencia);
                this.dispose();

            } else {
                JOptionPane.showMessageDialog(null, "Debe anadir al menos a un empleado", "Error ingreso de Datos", 2);
                anadir = new anadirPersona(agencia);
                anadir.setTitle("Agregar pasajero a " + agencia.getNombre());
                anadir.setVisible(true);
            }
        }
        
       
    }//GEN-LAST:event_btnAceptarActionPerformed

