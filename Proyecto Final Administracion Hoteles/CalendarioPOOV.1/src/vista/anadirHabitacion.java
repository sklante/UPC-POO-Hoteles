    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        Habitacion hab = (Habitacion) cbxHabitaciones.getSelectedItem();
        System.out.println(hab.getTipoHabitacion().getMoneda());
        System.out.println(hab.getTipoHabitacion().getPrecio());
        Fecha fec = ControladorHotel.formatoHaciaFecha(txtFechaEntrada.getText());
        int dias = Integer.parseInt(txtCantidadDias.getText());
        if (ControladorHotel.estaEnRangoFecha(12, fec)) {
            if (ControladorHotel.reservaRepetida(hab.toString(), fec.toFechaCompleta(), dias,"")) {
                if (ControladorHotel.esNulos((String) txtFechaEntrada.getText())
                        && ControladorHotel.esNulos((String) txtCantidadDias.getText().toString())
                        && cbxHabitaciones.getSelectedIndex() == -1) {
                    JOptionPane.showMessageDialog(this, "Por favor ingrese los datos solicitados", "Aviso de Error", 1);
                } else {
                    ControladorHotel.agregarReservaTabla(hab,
                            fec,
                            dias, 0.0, null);
                    ControladorHotel.AgregarReservaPreliminar(hab,
                            fec,
                            dias, null);
                    txtCantidadDias.setText("");
                    txtFechaEntrada.setText("");
                    cbxHabitaciones.setSelectedIndex(-1);
                    this.dispose();
                }
            }
        }
    }//GEN-LAST:event_btnAceptarActionPerformed
    
    private void cbxHabitacionesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxHabitacionesActionPerformed
        if (cbxHabitaciones.getSelectedIndex() != -1) {
            String tipo = ((Habitacion) cbxHabitaciones.getSelectedItem()).getTipoHabitacion().toString();
            String precio = String.valueOf(((Habitacion) cbxHabitaciones.getSelectedItem()).getTipoHabitacion().getPrecio());
            System.out.println(tipo);
            System.out.println(precio);
            String moneda = ((Habitacion) cbxHabitaciones.getSelectedItem()).getTipoHabitacion().getMoneda().getSimbolo();
            labPrecio.setText(tipo + " " + precio + "-" + moneda);
        } else {
            labPrecio.setText("");
        }
    }//GEN-LAST:event_cbxHabitacionesActionPerformed

