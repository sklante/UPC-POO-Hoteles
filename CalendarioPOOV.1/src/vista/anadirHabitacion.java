    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(ControladorHotel.esNulos((String)txtFechaEntrada.getText())&& 
                ControladorHotel.esNulos((String)txtCantidadDias.getText().toString()) &&
                cbxHabitaciones.getSelectedIndex()==-1){
            JOptionPane.showMessageDialog(this, "Por favor ingrese los datos solicitados","Aviso de Error", 1);
        }else{
            ControladorHotel.agregarReservaTabla((Habitacion)cbxHabitaciones.getSelectedItem(), 
                    ControladorHotel.formatoHaciaFecha(txtFechaEntrada.getText()), 
                    Integer.parseInt(txtCantidadDias.getText()));
            ControladorHotel.AgregarReservaPreliminar((Habitacion)cbxHabitaciones.getSelectedItem(), 
                    ControladorHotel.formatoHaciaFecha(txtFechaEntrada.getText()), 
                    Integer.parseInt(txtCantidadDias.getText()));
            txtCantidadDias.setText("");
            txtFechaEntrada.setText("");
            cbxHabitaciones.setSelectedIndex(-1);
            this.dispose();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

